/**
 * This class describes the ShootBehavior. It implements the interface OffenceBehavior and its play method
 * @author Jonathan Terry
 *
 */

public class ShootBehavior implements OffenceBehavior {
	/**
	 * @return This method returns a String that describes what happens during offence if ShootBehavior is chosen as the OffenceBehavior.
	 */
	public String play() {
		  return "shoots at the goal";
	}

}
