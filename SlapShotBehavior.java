/**
 * This class describes the SlapShotBehavior. It implements the interface OffenceBehavior and its play method
 * @author Jonathan Terry
 *
 */

public class SlapShotBehavior implements OffenceBehavior {
	/**
	 * @return This method returns a String that describes what happens during offence if SlapShotBehavior is chosen as the OffenceBehavior.
	 */
	public String play() {
		  return "shoots the puck from the blue line";
	}
	
}
